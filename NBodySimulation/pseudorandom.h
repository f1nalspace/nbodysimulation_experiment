const static uint32_t RANDOM_COLOR_TABLE_COUNT = ArrayCount(RANDOM_COLOR_TABLE);

struct RandomSeries {
	uint32_t intIndex;
	uint32_t colorIndex;
};

/* Creates a random series and uses the given seed as an initial index */
inline RandomSeries RandomSeed(uint32_t seed) {
	RandomSeries result;
	result.intIndex = seed % RANDOM_TABLE_COUNT;
	result.colorIndex = seed % RANDOM_COLOR_TABLE_COUNT;
	return(result);
}

/* Returns the next unsigned integer from the series and increases/wraps the counter */
inline uint32_t RandomNextUInt32(RandomSeries *series) {
	uint32_t result = RANDOM_TABLE[series->intIndex++];
	if (series->intIndex >= (RANDOM_TABLE_COUNT - 1)) {
		series->intIndex = 0;
	}
	return(result);
}

/* Returns the next RGBA color from the series and increases/wraps the counter */
inline uint32_t RandomNextRGBA(RandomSeries *series) {
	uint32_t result = RANDOM_COLOR_TABLE[series->colorIndex++];
	if (series->colorIndex >= (RANDOM_COLOR_TABLE_COUNT - 1)) {
		series->colorIndex = 0;
	}
	return(result);
}

/* Returns a random index from 0 to range - 1  */
inline uint32_t RandomIndex(RandomSeries *series, uint32_t range) {
	uint32_t result = RandomNextUInt32(series) % range;
	return(result);
}

/* Returns a random value between 0.0 and 1.0 */
inline float RandomUnilateral(RandomSeries *series) {
	uint32_t value = RandomNextUInt32(series) - RANDOM_TABLE_MIN_NUMBER;
	float divisor = 1.0f / (float)RANDOM_TABLE_RANGE_NUMBER;
	float result = value * divisor;
	return(result);
}

/* Returns a random value between -1.0 and 1.0 */
inline float RandomBilateral(RandomSeries *series) {
	uint32_t value = RandomNextUInt32(series) - RANDOM_TABLE_MIN_NUMBER;
	float divisor = 1.0f / (float)RANDOM_TABLE_RANGE_NUMBER;
	float result = 2.0f * RandomUnilateral(series) - 1.0f;
	return(result);
}

/* Returns a random float value between the given min and max */
inline float RandomBetweenFloat(RandomSeries *series, float min, float max) {
	float range = max - min;
	float result = min + RandomUnilateral(series) * range;
	return(result);
}

/* Returns a random integer value between the given min and max */
inline int32_t RandomBetweenInt(RandomSeries *series, int32_t min, int32_t max) {
	uint32_t range = max - min;
	int32_t result = min + (int32_t)(RandomUnilateral(series) * range);
	return(result);
}

/* Returns a random color vector, each component between 0.0 and 1.0 */
inline Vec4f RandomColor(RandomSeries *series) {
	uint32_t rgba = RandomNextRGBA(series);
	Vec4f result = RGBAToLinear(rgba);
	return(result);
}

